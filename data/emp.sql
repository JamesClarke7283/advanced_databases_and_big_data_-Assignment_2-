CREATE DATABASE IF NOT EXISTS company;

USE company;

CREATE TABLE IF NOT EXISTS emp (
  EMPNO INT AUTO_INCREMENT,
  ENAME VARCHAR(10),
  JOB VARCHAR(9),
  MGR INT,
  HIREDATE DATE,
  SAL DECIMAL(7,2),
  COMM DECIMAL(7,2),
  DEPTNO INT,
  CONSTRAINT pk_emp PRIMARY KEY (EMPNO),
  CONSTRAINT fk_emp_depts FOREIGN KEY (DEPTNO) REFERENCES dept (DEPTNO)
);

